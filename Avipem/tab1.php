<h3>Distrito de Mollebaya</h3>
<p class="text-black-50">El distrito de Mollebaya es uno de los 29 distritos que conforman la provincia de Arequipa en el Departamento de Arequipa, bajo la administración del Gobierno regional de Arequipa, en el sur del Perú.

Desde el punto de vista jerárquico de la Iglesia católica forma parte de la Arquidiócesis de Arequipa.</p>
